    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //
       String respuesta_usuario=txt_respuesta.getText();
       String respuesta_correcta=lista_preguntas.get(indice_pregunta).getSolucion();
       if (respuesta_correcta.equalsIgnoreCase(respuesta_usuario))
       {
           puntuacion++;
           actualizarPuntuacion();
       }
       if (indice_pregunta<lista_preguntas.size()-1)
       {
       indice_pregunta++;
       txt_respuesta.setText("");
       }
       else
       {
           //fin de partida, cambiar algo en la pantalla
           lbl_pregunta.setText("FIN DE LA PARTIDA");
       }
       actualizarPregunta();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String id_partida=(String)cmb_partida1.getSelectedItem();
        List<Pregunta> lista_preguntas=AccesoDatos.pedirPartida(id_partida);
        String aux="";
        for (Pregunta p:lista_preguntas)
        {
            aux+=p.crearPreguntaCompleta()+"->"+p.getSolucion()+"\n";
        }
        ta_preguntas.setText(aux);
    }//GEN-LAST:event_jButton3ActionPerformed

