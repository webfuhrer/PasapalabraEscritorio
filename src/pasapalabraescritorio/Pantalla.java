    private void rellenarCombo() {
        for (int i=15000; i<15500; i++)
        {
            cmb_partida.addItem(String.valueOf(i));
            cmb_partida1.addItem(String.valueOf(i));
        }  
    }

    private void jugar(String id_partida) {
        lista_preguntas=AccesoDatos.pedirPartida(id_partida);
        actualizarPregunta();
        
    }

    private void actualizarPregunta() {
       Pregunta p=lista_preguntas.get(indice_pregunta);
       String pregunta_completa=p.crearPreguntaCompleta();
       lbl_pregunta.setText(pregunta_completa);
    }

    private void actualizarPuntuacion() {
       lbl_puntuacion.setText(String.valueOf(puntuacion));
    }
